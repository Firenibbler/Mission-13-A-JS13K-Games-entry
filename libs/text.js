g.dT = function (str, X, Y, sz, cnt, clr) {
    sz = sz || 5;
    sz = sz * g.scf;
    var needed = [];
    str = str.toUpperCase(); // because I only did uppercase letters
    for (var i = 0; i < str.length; i++) {
        var letter = letters[str.charAt(i)];
        if (letter) { // because there's letters I didn't do
            needed.push(letter);
        }
    }
    var width = 0;
    var offsetX = 0;
    if (cnt) {

        for (i = 0; i < needed.length; i++) {
            for (i = 0; i < needed.length; i++) {
                letter = needed[i];
                var currY = Y;
                var addX = 0;
                for (var y = 0; y < letter.length; y++) {
                    var row = letter[y];
                    addX = Math.max(addX, row.length * sz);
                }
                width += sz + addX;
            }
        }

    }
    X = X - width / 2;
    g.ctx.fillStyle = clr || "#FFFFFF";
    var currX = X;
    for (i = 0; i < needed.length; i++) {
        letter = needed[i];
        var currY = Y;
        var addX = 0;
        for (var y = 0; y < letter.length; y++) {
            var row = letter[y];
            for (var x = 0; x < row.length; x++) {
                if (row[x]) {
                    g.ctx.fillRect(currX + x * sz, currY, sz, sz);
                }
            }
            addX = Math.max(addX, row.length * sz);
            currY += sz;
        }
        currX += sz + addX;
        width += sz + addX;
    }
    return width;
}
